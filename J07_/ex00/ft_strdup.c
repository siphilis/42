#include <stdlib.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	char *copy;

	copy = (char *)malloc(sizeof(*src) * ft_strlen(src) + 1);
	copy = src;
	return (copy);
}
